let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);

    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update page title based on current page
    const pageKey = document.body.dataset.pageKey || 'pageTitle';
    document.title = translations[lang][pageKey] || translations[lang]['pageTitle'];

    // Update current language display in dropdown
    const currentLangElement = document.getElementById('currentLanguage');
    if(currentLangElement) {
       currentLangElement.textContent = lang.toUpperCase();
    }
}

// Language switcher event listeners
document.querySelectorAll('.language-switcher').forEach(button => {
    button.addEventListener('click', (event) => {
        event.preventDefault();
        setLanguage(event.target.dataset.lang);
    });
});

// Animations on scroll using Intersection Observer
const animateOnScrollElements = document.querySelectorAll('.animated-on-scroll');

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const animationClass = entry.target.dataset.animation;
            if (animationClass) {
                entry.target.classList.add(animationClass, 'animate');
            }
            // Apply delay if specified
            const delay = entry.target.dataset.delay;
            if (delay) {
                entry.target.style.setProperty('--data-delay', delay);
            }
            observer.unobserve(entry.target); // Stop observing once animated
        }
    });
}, { threshold: 0.1 }); // Trigger when 10% of the element is visible

animateOnScrollElements.forEach(element => {
    observer.observe(element);
});

// Contact Form Validation
const contactForm = document.getElementById('contactForm');
if (contactForm) {
    contactForm.addEventListener('submit', function(event) {
        event.preventDefault(); // Prevent default form submission

        let isValid = true;

        const nameInput = document.getElementById('contactName');
        const emailInput = document.getElementById('contactEmail');
        const messageInput = document.getElementById('contactMessage');

        // Validate Name
        if (nameInput.value.trim() === '') {
            nameInput.classList.add('is-invalid');
            nameInput.nextElementSibling.textContent = translations[currentLanguage].contactNameError;
            isValid = false;
        } else {
            nameInput.classList.remove('is-invalid');
        }

        // Validate Email
        const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailPattern.test(emailInput.value.trim())) {
            emailInput.classList.add('is-invalid');
            emailInput.nextElementSibling.textContent = translations[currentLanguage].contactEmailError;
            isValid = false;
        } else {
            emailInput.classList.remove('is-invalid');
        }

        // Validate Message
        if (messageInput.value.trim() === '') {
            messageInput.classList.add('is-invalid');
            messageInput.nextElementSibling.textContent = translations[currentLanguage].contactMessageError;
            isValid = false;
        } else {
            messageInput.classList.remove('is-invalid');
        }

        if (isValid) {
            // If valid, you would typically send the form data, e.g., using fetch API
            alert('Form submitted successfully! (This is a demo, no actual submission)');
            contactForm.reset(); // Clear form fields
            // Optionally, hide the form and show a success message
        }
    });

    // Remove invalid class on input
    document.querySelectorAll('#contactForm .form-control').forEach(input => {
        input.addEventListener('input', () => {
            input.classList.remove('is-invalid');
        });
    });
}

// Scroll-to-Top Button
const scrollToTopBtn = document.getElementById("scrollToTopBtn");

window.addEventListener("scroll", () => {
    if (window.scrollY > 300) { // Show button after scrolling down 300px
        scrollToTopBtn.classList.add("show");
    } else {
        scrollToTopBtn.classList.remove("show");
    }
});

scrollToTopBtn.addEventListener("click", () => {
    window.scrollTo({
        top: 0,
        behavior: "smooth"
    });
});

